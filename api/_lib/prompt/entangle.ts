export function buildEntangleSystemPrompt(
  goodCaseIndices: readonly number[] = [0, 1, 2, 3, 4, 5, 6, 7, 8],
  badCaseIndices: readonly number[] = [0, 1, 2, 3]
): string {
  // 如果使用默认参数，直接返回预构建的静态 prompt，避免每次重新拼接
  if (
    goodCaseIndices.length === 9 &&
    badCaseIndices.length === 4 &&
    goodCaseIndices[0] === 0 &&
    badCaseIndices[0] === 0
  ) {
    return FULL_SYSTEM_PROMPT;
  }
  return buildBaseSystemPrompt(goodCaseIndices, badCaseIndices);
}

export function buildEntangleUserPrompt(
  termA: string,
  termB: string,
  regenerate: boolean,
  strategy: EntangleStrategy
): string {
  const regenerateRule = regenerate
    ? "这是重新生成。必须显著避开第一直觉路线，优先换妙感类型、换枢纽节点、换约束条件。"
    : "默认目标是做出用户愿意看完、愿意截图分享的路径，而不是最快把两个词解释通。";

  const strategyRule = buildStrategyInstruction(strategy);

  return [
    "【本次任务】",
    `起点：${termA}`,
    `终点：${termB}`,
    "",
    "【本轮要求】",
    strategyRule,
    regenerateRule,
    "不要先在脑中铺很多候选再裁切；直接给出你最成立的 1 条路径。",
    "先确定妙感类型和枢纽节点，再向两端延展。",
    "优先选择能让起点与终点都留下现实痕迹的链路，不要只抓一个通用载体就往另一端硬跳。",
    "硬中介可以使用，但只是现实锚点参考，不是默认答案。",
  ].join("\n");
}

